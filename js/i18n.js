  /* ─────────────────────────────────────────────────
     Función principal: aplica el idioma elegido
  ───────────────────────────────────────────────── */
const DEFAULT_LANG = 'en_US';
const LANG_STORAGE_KEY = 'biotrack-lang';
const LANGUAGE_MAP = {
  en_US: { translationKey: 'en', htmlLang: 'en-US' },
  es_419: { translationKey: 'es', htmlLang: 'es-419' },
  en: { translationKey: 'en', htmlLang: 'en-US', canonical: 'en_US' },
  es: { translationKey: 'es', htmlLang: 'es-419', canonical: 'es_419' },
};

function getLanguageConfig(lang) {
    const requestedLang = LANGUAGE_MAP[lang] ? lang : DEFAULT_LANG;
    const config = LANGUAGE_MAP[requestedLang];
    return {
      code: config.canonical || requestedLang,
      translationKey: config.translationKey,
      htmlLang: config.htmlLang,
    };
}

function applyLanguage(lang) {
    const config = getLanguageConfig(lang);
    const t = translations[config.translationKey];
    if (!t) return;
 
    /* 1. Atributo lang del HTML (SEO) */
    document.documentElement.lang = config.htmlLang;
 
    /* 2. <title> (usa data-i18n cuando exista) */
    const titleEl = document.querySelector('title');
    if (titleEl) {
      const titleKey = titleEl.getAttribute('data-i18n') || 'page-title';
      if (t[titleKey] !== undefined) titleEl.textContent = t[titleKey];
    }
 
    /* 3. Meta description */
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc && t["meta-description"]) metaDesc.setAttribute('content', t["meta-description"]);
 
    /* 4. Todos los elementos con data-i18n */
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (t[key] !== undefined) el.textContent = t[key];
    });

    /* 4.1 Placeholders */
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      if (t[key] !== undefined) el.setAttribute('placeholder', t[key]);
    });

    /* 4.2 Aria-label */
    document.querySelectorAll('[data-i18n-aria-label]').forEach(el => {
      const key = el.getAttribute('data-i18n-aria-label');
      if (t[key] !== undefined) el.setAttribute('aria-label', t[key]);
    });

    /* 4.3 Alt text */
    document.querySelectorAll('[data-i18n-alt]').forEach(el => {
      const key = el.getAttribute('data-i18n-alt');
      if (t[key] !== undefined) el.setAttribute('alt', t[key]);
    });
 
    /* 5. Estado visual del selector de idioma */
    document.querySelectorAll('.lang-btn').forEach(btn => {
      const btnConfig = getLanguageConfig(btn.dataset.lang);
      const isActive = btnConfig.code === config.code;
      btn.classList.toggle('active', isActive);
      btn.setAttribute('aria-pressed', isActive);
    });
 
    /* 6. Persistencia */
    localStorage.setItem(LANG_STORAGE_KEY, config.code);

    /* 7. Evento para scripts de página */
    document.dispatchEvent(new CustomEvent('biotrack:lang-changed', {
      detail: { lang: config.code }
    }));
  }
 
  /* ─────────────────────────────────────────────────
     Inicialización: lee preferencia guardada o usa inglés
  ───────────────────────────────────────────────── */
(function init() {
    const savedLang = localStorage.getItem(LANG_STORAGE_KEY);
    applyLanguage(savedLang || DEFAULT_LANG);
})();
